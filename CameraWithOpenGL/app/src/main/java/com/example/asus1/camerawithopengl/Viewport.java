package com.example.asus1.camerawithopengl;

public class Viewport {
    public int x;
    public int y;
    public int width;
    public int height;

    public Viewport(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public Viewport() {
    }


}
